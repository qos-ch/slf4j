package org.slf4j.helpers;

/**
 * This class is used internally by BogoPerf, hence the package private
 * (default) access.
 * 
 * @author Ceki
 */
class BubbleSort {

    static void sort(int[] a) {
        int len = a.length;
        for (int i = 0; i < len - 1; i++) {
            for (int j = 0; j < len - 1 - i; j++) {
                if (a[j] > a[j + 1]) {
                    swap(a, j, j + 1);
                }
            }
        }
    }

    static void swap(int[] a, int i, int j) {
        int t = a[i];
        a[i] = a[j];
        a[j] = t;
    }
}
